@app.get("/api/v1/status/{task_id}")
async def get_task_status(task_id: str):
    """작업 상태 조회"""
    if task_id in task_results:
        result = task_results[task_id]
        status = result.get("status", "unknown")

        response = {
            "task_id": task_id,
            "status": status,
        }

        if status == "completed":
            response["progress"] = 100
            response["message"] = "✅ 처리 완료"
            if "output_file" in result:
                response["output_file"] = result["output_file"]
                response["file_type"] = result.get("file_type", "unknown")
        elif status == "processing":
            response["progress"] = 50
            response["message"] = "⏳ 처리 중..."
        elif status == "error":
            response["progress"] = 0
            response["message"] = f"❌ 오류: {result.get('error', 'Unknown error')}"
        else:
            response["progress"] = 0
            response["message"] = "상태 불명"

        return JSONResponse(response)
    else:
        return JSONResponse({
            "task_id": task_id,
            "status": "not_found",
            "progress": 0,
            "message": "작업을 찾을 수 없습니다"
        }, status_code=404)

@app.get("/api/v1/download/{task_id}")
async def download_output(task_id: str):
    """생성된 파일 다운로드"""
    if task_id not in task_results:
        raise HTTPException(status_code=404, detail=f"Task '{task_id}' not found")

    result = task_results[task_id]
    if result.get("status") != "completed":
        raise HTTPException(status_code=400, detail=f"Task '{task_id}' is not completed yet")

    output_file = result.get("output_file")
    if not output_file:
        raise HTTPException(status_code=400, detail=f"No output file found for task '{task_id}'")

    file_path = Path(output_file)
    if not file_path.exists():
        raise HTTPException(status_code=404, detail=f"Output file not found: {output_file}")

    return FileResponse(
        path=file_path,
        media_type="application/octet-stream",
        filename=file_path.name
    )

@app.get("/api/v1/outputs")
async def list_outputs():
    """생성된 파일 목록"""
    outputs = []

    if OUTPUT_DIR.exists():
        for file in OUTPUT_DIR.iterdir():
            if file.is_file():
                outputs.append({
                    "filename": file.name,
                    "size": file.stat().st_size,
                    "created": datetime.fromtimestamp(file.stat().st_ctime).isoformat()
                })

    return JSONResponse({
        "total": len(outputs),
        "outputs": outputs
    })

# ==================== Skill Details ====================

@app.get("/api/v1/skill/{skill_name}")
async def get_skill_info(skill_name: str):
    """스킬 정보 조회"""
    skill_path = SENSENOVA_SKILLS_PATH / "skills" / skill_name

    if not skill_path.exists():
        raise HTTPException(status_code=404, detail=f"Skill '{skill_name}' not found")

    skill_md = skill_path / "SKILL.md"

    info = {
        "name": skill_name,
        "exists": True,
        "path": str(skill_path),
        "has_skill_md": skill_md.exists(),
        "structure": []
    }

    # 디렉토리 구조 읽기
    for item in skill_path.iterdir():
        info["structure"].append(item.name)

    return JSONResponse(info)

