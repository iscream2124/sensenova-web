@app.get("/health", response_model=HealthResponse)
async def health_check():
    """서버 상태 확인"""
    skills_available = SENSENOVA_SKILLS_PATH.exists()

    return HealthResponse(
        status="healthy",
        version="1.0.0",
        skills_path=str(SENSENOVA_SKILLS_PATH),
        available=skills_available
    )

@app.get("/skills", response_model=SkillListResponse)
async def list_skills():
    """설치된 스킬 목록"""
    skills = get_available_skills()

    return SkillListResponse(
        available_skills=skills,
        total_count=len(skills)
    )

# ==================== Image Generation ====================

@app.post("/api/v1/infographic", response_model=InfographicResponse)
async def generate_infographic(request: InfographicRequest):
    """
    인포그래픽 생성

    Parameters:
    - user_prompt: 생성할 인포그래픽의 설명
    - max_rounds: 생성 라운드 수 (기본값: 1)
    - output_mode: 출력 모드 (friendly/verbose)
    - prompts_expand_mode: 프롬프트 확장 전략 (auto/force/disable)
    """
    task_id = generate_task_id()

    try:
        # 동기로 파일 생성
        generate_sample_output("sn-infographic", task_id, {
            "user_prompt": request.user_prompt,
            "max_rounds": request.max_rounds,
            "output_mode": request.output_mode,
            "prompts_expand_mode": request.prompts_expand_mode
        })

        return InfographicResponse(
            status="success",
            task_id=task_id,
            message=f"✅ Infographic generated successfully: {request.user_prompt[:50]}...",
            image_path=str(OUTPUT_DIR / f"{task_id}_infographic.svg"),
            metadata={
                "max_rounds": request.max_rounds,
                "mode": request.output_mode,
                "prompt_len": len(request.user_prompt)
            }
        )
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

# ==================== Presentation Generation ====================

@app.post("/api/v1/ppt", response_model=PPTResponse)
async def generate_ppt(request: PPTRequest):
    """
    PPT 생성

    Parameters:
    - title: 프레젠테이션 제목
    - content: 프레젠테이션 내용
    - mode: 생성 모드 (creative/standard)
    - page_count: 슬라이드 수
    - style: 스타일 (선택사항)
    """
    task_id = generate_task_id()

    try:
        generate_sample_output("sn-ppt-entry", task_id, {
            "title": request.title,
            "content": request.content,
            "mode": request.mode,
            "page_count": request.page_count,
            "style": request.style
        })

        return PPTResponse(
            status="success",
            task_id=task_id,
            message=f"PPT generated successfully for '{request.title}'",
            pptx_path=str(OUTPUT_DIR / f"{task_id}_presentation.txt")
        )
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

# ==================== Data Analysis ====================

@app.post("/api/v1/analysis")
async def analyze_data(
    file: UploadFile = File(...),
    analysis_type: str = Form("summary")
):
    """
    데이터 분석

    Parameters:
    - file: 분석할 파일 (Excel, CSV)
    - analysis_type: 분석 유형 (summary/detailed/export)
    """
    task_id = generate_task_id()

    try:
        # 임시 파일로 저장
        with tempfile.NamedTemporaryFile(delete=False, suffix=file.filename) as tmp:
            content = await file.read()
            tmp.write(content)
            tmp_path = tmp.name

        generate_sample_output("sn-da-excel-workflow", task_id, {
            "file_path": tmp_path,
            "analysis_type": analysis_type
        })

        return JSONResponse({
            "status": "success",
            "task_id": task_id,
            "message": f"Data analysis completed for {file.filename}",
            "file_name": file.filename,
            "analysis_type": analysis_type,
            "output_file": str(OUTPUT_DIR / f"{task_id}_analysis.txt")
        })
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

