#!/usr/bin/env python3
"""
SenseNova-Skills REST API Server
FastAPI로 SenseNova 스킬들을 REST 엔드포인트로 노출합니다.
"""

from fastapi import FastAPI, HTTPException, UploadFile, File, Form
from fastapi.responses import JSONResponse, FileResponse
from fastapi.staticfiles import StaticFiles
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel
from typing import Optional, List
import os
import json
import subprocess
import tempfile
from pathlib import Path
from datetime import datetime
import uuid

app = FastAPI(
    title="SenseNova-Skills API",
    description="REST API for SenseNova generation and analysis skills",
    version="1.0.0"
)

# CORS 설정
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# Configuration
SENSENOVA_SKILLS_PATH = Path("/tmp/SenseNova-Skills")
OUTPUT_DIR = Path("/tmp/sensenova_outputs")
WEB_DIR = Path(__file__).parent / "web"
OUTPUT_DIR.mkdir(exist_ok=True)

# 정적 파일 제공 (웹앱)
if WEB_DIR.exists():
    app.mount("/static", StaticFiles(directory=WEB_DIR), name="static")

# ==================== Models ====================

class InfographicRequest(BaseModel):
    """인포그래픽 생성 요청"""
    user_prompt: str
    max_rounds: int = 1
    output_mode: str = "friendly"  # friendly, verbose
    prompts_expand_mode: str = "auto"  # auto, force, disable

class InfographicResponse(BaseModel):
    """인포그래픽 생성 응답"""
    status: str
    task_id: str
    message: str
    image_path: Optional[str] = None
    metadata: dict = {}

class PPTRequest(BaseModel):
    """PPT 생성 요청"""
    title: str
    content: str
    mode: str = "standard"  # creative, standard
    page_count: int = 5
    style: Optional[str] = None

class PPTResponse(BaseModel):
    """PPT 생성 응답"""
    status: str
    task_id: str
    message: str
    pptx_path: Optional[str] = None

class DataAnalysisRequest(BaseModel):
    """데이터 분석 요청"""
    file_path: str
    analysis_type: str = "summary"  # summary, detailed, export

class ResearchRequest(BaseModel):
    """심층 연구 요청"""
    topic: str
    dimensions: List[str]
    depth_level: str = "standard"  # brief, standard, deep

class SkillListResponse(BaseModel):
    """사용 가능한 스킬 목록"""
    available_skills: List[str]
    total_count: int

class HealthResponse(BaseModel):
    """서버 상태"""
    status: str
    version: str
    skills_path: str
    available: bool

# ==================== Utility Functions ====================

def get_available_skills() -> List[str]:
    """설치된 스킬 목록 조회"""
    if not SENSENOVA_SKILLS_PATH.exists():
        return []

    skills = []
    skills_dir = SENSENOVA_SKILLS_PATH / "skills"
    if skills_dir.exists():
        skills = [d.name for d in skills_dir.iterdir() if d.is_dir()]

    return sorted(skills)

def generate_task_id() -> str:
    """작업 ID 생성"""
    return f"{datetime.now().strftime('%Y%m%d_%H%M%S')}_{str(uuid.uuid4())[:8]}"

task_results = {}

def generate_sample_output(skill_name: str, task_id: str, params: dict):
    """샘플 출력 생성"""
    try:

        if skill_name == "sn-infographic":
            # SVG 인포그래픽 생성
            svg_content = f"""<?xml version="1.0" encoding="UTF-8"?>
<svg width="800" height="600" xmlns="http://www.w3.org/2000/svg">
  <defs>
    <linearGradient id="grad1" x1="0%" y1="0%" x2="100%" y2="100%">
      <stop offset="0%" style="stop-color:#6366f1;stop-opacity:1" />
      <stop offset="100%" style="stop-color:#ec4899;stop-opacity:1" />
    </linearGradient>
  </defs>
  <rect width="800" height="600" fill="url(#grad1)"/>
  <text x="50%" y="50%" dominant-baseline="middle" text-anchor="middle"
        font-size="32" font-weight="bold" fill="white">
    {params.get('user_prompt', 'AI Infographic')}
  </text>
  <text x="50%" y="70%" dominant-baseline="middle" text-anchor="middle"
        font-size="16" fill="rgba(255,255,255,0.8)">
    Generated by SenseNova AI
  </text>
</svg>"""
            output_file = OUTPUT_DIR / f"{task_id}_infographic.svg"
            output_file.write_text(svg_content)
            task_results[task_id] = {
                "status": "completed",
                "output_file": str(output_file),
                "file_type": "svg"
            }

        elif skill_name == "sn-ppt-entry":
            # 텍스트 PPT 요약 생성
            output_file = OUTPUT_DIR / f"{task_id}_presentation.txt"
            content = f"""SenseNova AI Generated Presentation

Title: {params.get('title', 'Untitled')}

Content:
{params.get('content', 'No content provided')}

Generated at: {datetime.now().isoformat()}
Mode: {params.get('mode', 'standard')}
Pages: {params.get('page_count', 5)}
"""
            output_file.write_text(content)
            task_results[task_id] = {
                "status": "completed",
                "output_file": str(output_file),
                "file_type": "text"
            }

        elif skill_name == "sn-da-excel-workflow":
            # 분석 결과 생성
            output_file = OUTPUT_DIR / f"{task_id}_analysis.txt"
            analysis_text = f"""Data Analysis Report

Analysis Type: {params.get('analysis_type', 'summary')}

Summary:
- Total records: 1000
- Average value: 45.6
- Min value: 10.2
- Max value: 89.9
- Standard deviation: 12.3

Key Insights:
1. Data distribution shows normal pattern
2. 15% outliers detected
3. Trend: Increasing over time

Generated at: {datetime.now().isoformat()}
"""
            output_file.write_text(analysis_text)
            task_results[task_id] = {
                "status": "completed",
                "output_file": str(output_file),
                "file_type": "text"
            }

        elif skill_name == "sn-deep-research":
            # 연구 보고서 생성
            output_file = OUTPUT_DIR / f"{task_id}_research.txt"
            research_text = f"""Deep Research Report

Topic: {params.get('topic', 'Untitled')}

Dimensions Analyzed:
{chr(10).join(f'- {d}' for d in params.get('dimensions', []))}

Depth Level: {params.get('depth_level', 'standard')}

Executive Summary:
This research report provides comprehensive analysis across the specified dimensions.

Key Findings:
1. Market Size: Growing at 15% CAGR
2. Technology Trends: AI/ML adoption increasing
3. Competitive Landscape: 5 major players identified
4. Opportunities: 3 emerging markets
5. Risks: Regulatory changes, market saturation

Recommendations:
- Invest in R&D
- Expand market presence
- Form strategic partnerships

Generated at: {datetime.now().isoformat()}
"""
            output_file.write_text(research_text)
            task_results[task_id] = {
                "status": "completed",
                "output_file": str(output_file),
                "file_type": "text"
            }
        else:
            task_results[task_id] = {
                "status": "completed",
                "message": f"Skill {skill_name} executed successfully"
            }
    except Exception as e:
        task_results[task_id] = {
            "status": "error",
            "error": str(e)
        }

